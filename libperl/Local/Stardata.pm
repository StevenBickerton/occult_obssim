my %data_set = ("V" => $V_stars, "III" => $III_stars, "I" => $I_stars);
foreach my $lum_class ( keys %data_set ) {

    my @lines = split /\n/, $data_set{$lum_class};

    foreach my $line (@lines) {

      my ($code,  $spectype, $teff, $L, $R, $M, $Mbol, $BC, 
	  $Mv, $UmB, $BmV, $VmR, $RmI);
      if ($lum_class =~ /V/) {
	($code, $spectype, $teff, $L, $R, $M, $Mbol, $BC, 
	 $Mv, $UmB, $BmV, $VmR, $RmI) = split /\s+/, $line;
      } else {
	($code, $spectype, $teff, $L, $R, $M, $Mbol, 
	 $BC, $Mv, $UmB, $BmV) = split /\s+/, $line;
	($VmR,$RmI) = (-99,-99);
      }
      $star{"$spectype"."$lum_class"}{"Teff"} = $teff;
      $star{"$spectype"."$lum_class"}{"L"} = $L;
      $star{"$spectype"."$lum_class"}{"R"} = $R;
      $star{"$spectype"."$lum_class"}{"M"} = $M;
      $star{"$spectype"."$lum_class"}{"Mbol"} = $Mbol;
      $star{"$spectype"."$lum_class"}{"BC"} = $BC;
      $star{"$spectype"."$lum_class"}{"Mv"} = $Mv;
      $star{"$spectype"."$lum_class"}{"UmB"} = $UmB;
      $star{"$spectype"."$lum_class"}{"BmV"} = $BmV;
      $star{"$spectype"."$lum_class"}{"VmR"} = $VmR;
      $star{"$spectype"."$lum_class"}{"RmI"} = $RmI;
      $star{"$spectype"."$lum_class"}{"MK"} = $spectype;
      $star{"$spectype"."$lum_class"}{"code"} = $code;
      
      $starcode{"$code"."$lum_class"} = $star{"$spectype"."$lum_class"};
    }

}


